#include<bits/stdc++.h>
using namespace std;
void convert(int n,int a[])
{
	int count=0;
	for(int i=0;i<n;i++)
	{
		for(int j=i-1;j>=0;j--)
		{
			if(a[i]==-a[j])
			{
				count++;
			}
		}
	}
	cout << count;
}
int main()
{
	int n=0;
	cin >> n;
	int a[n]={0};
	for(int i=0;i<n;i++)
	{
		cin >> a[i];
	}
	convert(n,a);
	return 0;
}
